package DTO;

import java.util.Objects;

/**
 *
 * @author Younes
 */
public class SwapiDTO {
    
    private String name; 
    private String url; 

    public SwapiDTO(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public SwapiDTO() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    @Override
    public String toString() {
        return "SwapiDTO{" + "name=" + name + ", url=" + url + '}';
    }

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final SwapiDTO other = (SwapiDTO) obj;
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        if (!Objects.equals(this.url, other.url)) {
            return false;
        }
        return true;
    }
    
    
}
